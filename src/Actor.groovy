class Actor {
    private int age
    private String name
    private boolean gender

    int getAge() {
        return age
    }

    void setAge(int age) {
        this.age = age
    }

    String getName() {
        return name
    }

    void setName(String name) {
        this.name = name
    }

    boolean getGender() {
        return gender
    }

    void setGender(boolean gender) {
        this.gender = gender
    }
}
